def get_all_videos() -> list[dict]:
    """Return all video definitions as a list."""
    return [{"id": k, **v} for k, v in SERIES.items()]


def get_videos_by_series(series_name: str) -> list[dict]:
    """Get all videos in a specific series."""
    return [{"id": k, **v} for k, v in SERIES.items() if v["series"] == series_name]


def get_series_names() -> list[str]:
    """Get unique series names."""
    return list(set(v["series"] for v in SERIES.values()))
